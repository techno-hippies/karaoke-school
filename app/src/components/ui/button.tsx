import * as React from "react"
import { Slot } from "@radix-ui/react-slot"
import { cva, type VariantProps } from "class-variance-authority"

import { cn } from "@/lib/utils"

const buttonVariants = cva(
  "inline-flex items-center justify-center gap-2 whitespace-nowrap rounded-full text-base font-medium transition-all duration-200 focus-visible:outline-none focus-visible:ring-2 disabled:pointer-events-none disabled:opacity-50 cursor-pointer active:scale-95",
  {
    variants: {
      variant: {
        default: "bg-primary text-primary-foreground hover:bg-primary/80 hover:shadow-lg",
        destructive: "bg-destructive text-destructive-foreground hover:bg-destructive/80",
        outline: "bg-secondary/30 text-foreground hover:bg-secondary/50",
        secondary: "bg-secondary text-secondary-foreground hover:bg-secondary/80",
        ghost: "text-foreground hover:bg-secondary/50",
        link: "text-primary underline-offset-4 hover:underline",
        // Gradient variants
        gradient: "bg-[image:var(--gradient-primary)] text-white hover:brightness-110 hover:shadow-[var(--glow-primary)]",
        "gradient-success": "bg-[image:var(--gradient-success)] text-white hover:brightness-110 hover:shadow-[var(--glow-success)]",
        "gradient-fire": "bg-[image:var(--gradient-fire)] text-white hover:brightness-110 hover:shadow-[var(--glow-fire)]",
        "gradient-gold": "bg-[image:var(--gradient-gold)] text-black hover:brightness-110 hover:shadow-[var(--glow-gold)]",
        // Recording state with pulse
        recording: "bg-[image:var(--gradient-fire)] text-white animate-glow-pulse",
      },
      size: {
        default: "h-11 px-4",
        sm: "h-10 px-3",
        lg: "h-12 px-8",
        xl: "h-14 px-10 text-lg",
        icon: "h-11 w-11",
      },
    },
    defaultVariants: {
      variant: "default",
      size: "default",
    },
  }
)

export interface ButtonProps
  extends React.ButtonHTMLAttributes<HTMLButtonElement>,
    VariantProps<typeof buttonVariants> {
  asChild?: boolean
}

const Button = React.forwardRef<HTMLButtonElement, ButtonProps>(
  ({ className, variant, size, asChild = false, ...props }, ref) => {
    const Comp = asChild ? Slot : "button"
    return (
      <Comp
        className={cn(buttonVariants({ variant, size, className }))}
        ref={ref}
        {...props}
      />
    )
  }
)
Button.displayName = "Button"

export { Button }